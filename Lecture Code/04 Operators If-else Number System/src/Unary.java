public class Unary {
    public static void main(String[] args) {
        int a = 5;
        int b = -a;
        System.out.println(b);

        int p = 5;
        System.out.println(p++);
        System.out.println(p);
        System.out.println(++p);
        System.out.println(p);

        int q = 10;
        System.out.println(q--);
        System.out.println(q);
        System.out.println(--q);
        System.out.println(q);
    }
}
